print("## Many Values to Multiple Variables ##")
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

print("## One Value to Multiple Variables ##")
x = y = z = "Orange"
print(x)
print(y)
print(z)

print("## Unpack a Collection ##")
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)



